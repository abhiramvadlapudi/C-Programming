//Check if a character is a vowel using multiple ifs.
#include<stdio.h>
int main() 
{
    char ch;
    printf("ch=");
    scanf("%c",&ch);
    if(ch=='a'||ch=='A')
    {
        printf("ch is vowel");
    }
    if(ch=='e'||ch=='E')
    {
        printf("ch is vowel");
    }
    if(ch=='i'||ch=='I')
    {
        printf("ch is vowel");
    }
    if(ch=='o'||ch=='O')
    {
        printf("ch is vowel");
    }
    if(ch=='u'||ch=='U')
    {
        printf("ch is vowel");
    }
}